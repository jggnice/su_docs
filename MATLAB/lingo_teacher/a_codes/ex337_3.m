% 大学数学实验2  p.63  教材例3  数值计算无穷区间广义积分： 区间:[ 0, inf ), f(x) = exp(- x*x )
% 精度 eps0 = 1e-6
% 精确值  I = sqrt(pi)/2 = 0.88622692545276
% 辛普森公式             z =  0.88622692029591，误差 = -5.156850946264058e-009 在1e-6内
format long	% 以下结果显示为双精度格式
I = sqrt(pi)/2
eps0 = 1e-6;	% 控制的精度值
eps1 = eps0/10
N = sqrt(-log(eps1))	% [N, inf ) 的积分值 < eps1/N ,  也可直接将N取为4 
z = quad('exp(- x.^2 )', 0, N, eps1)  	%辛普森公式, 误差限 1e-7 
z_I = z - I		% 近似计算的误差
format short