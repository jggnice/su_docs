% 迭代求解 x^2 + x - 14 = 0

% 求精确解的3行，2008a不能执行
syms x;
x12 = solve('x^2 + x -14 = 0',x)
x12v=vpa(x12)	% 精确解	    2008a不能做

x0 = 3;		% 初值
x1 = x0; x2 = x0; x3 = x0;
for i = 1:6
    x1 = 14 - x1^2;			%迭代公式1
    x2 = 14/(x2 + 1);			%迭代公式2
    x3 = x3 - (x3^2 + x3 -14)/(2*x3 + 1);%迭代公式3
    xa(1:3, i) = [x1; x2; x3] ;
end
