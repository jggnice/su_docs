function y = ex624f(x,a,b,c,d)	% 建立名为ex624f的函数，a, b, c d为F函数中的参数
% 大学数学实验  p.122 例2 用迭代公式 (28) 求解方程组，p.124 fsolve   
% 迭代求解 F(x) = {x1^2 + x2^2 = 4, x1^2 - x2^2 = 1}， 初值(1,1)'
%                   F'(x) = 2{x1  x2; x1,  -x2}
y(1) = x(1)^2 + a*x(2)^2 - b;	% a =  1, b = 4时为第1个方程
y(2) = x(1)^2 + c*x(2)^2 - d;	% c = -1, d = 1时为第2个方程

