% 拟合c( t ) = r e(Ck t) 中的系数 r,  k 
% r -- x(1),  k -- x(2)
% 初值 [ 10, 0.5 ] 

x0 = [ 10, 0.5];
t = [ 0.25  0.5  1  1.5  2  3  4  6  8 ];
c = [ 19.21 18.15  15.36  14.10  12.89  9.32  7.45  5.24  3.01 ];

f1 = inline( 'x(1) * exp( -x(2) * t ) ', 'x',  't')
[x1, rnorm1, res1] = lsqcurvefit(f1, x0, t, c)

f2 = inline( 'x(1) * exp( -x(2) * t ) - c', 'x',  't', 'c')
[x2, rnorm2, res2] = lsqnonlin(f2, x0,[], [], [], t, c)
