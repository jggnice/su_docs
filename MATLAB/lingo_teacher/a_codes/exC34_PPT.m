% 大学数学实验 PPT 12.26
% 粮食加工厂的一台自动装包机的设定值为: 每包装 
% 50 公斤, 标准差 0.3。现抽查了20包, 得到如下数据 
% 问该包装机运转是否正常？

x = [49.8  50.1  50.5  49.7  49.0  50.0  50.3  50.0  49.9  49.9 ...
     50.5  49.2  49.7  49.8  50.1  50.0  50.3  50.2  50.4  50.1];
sigma0 = 0.3;  alpha = 0.05;
s2 = var(x);
n = length(x);
chi2 = s2*(n-1)/sigma0^2
chi2_1_alpha = chi2inv(1- alpha,n-1)
if chi2 > chi2_1_alpha	
   disp('拒绝H0，认为方差显著大于0.09')
else
   disp('不能拒绝H0,认为方差不大于0.09')
end

